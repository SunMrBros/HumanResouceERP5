package com.hebut.erp.pojo;

public class SalaryDTO {
	private int empno;
	private String name;
	private float quanqin;
	private int latetimes;
	private float fakuan;
	private float other;
	private float money;
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getQuanqin() {
		return quanqin;
	}
	public void setQuanqin(float quanqin) {
		this.quanqin = quanqin;
	}
	public int getLatetimes() {
		return latetimes;
	}
	public void setLatetimes(int latetimes) {
		this.latetimes = latetimes;
	}
	public float getFakuan() {
		return fakuan;
	}
	public void setFakuan(float fakuan) {
		this.fakuan = fakuan;
	}
	public float getOther() {
		return other;
	}
	public void setOther(float other) {
		this.other = other;
	}
	public float getMoney() {
		return money;
	}
	public void setMoney(float money) {
		this.money = money;
	}
	
}
